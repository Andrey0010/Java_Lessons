package start.Lesson_4_Ternary_Switch_Condition;

public class Conditions {
    public static void main(String[] args) {
        int x = 5;
        int y = 5;

        if (x > y) {
            System.out.println("x больше, чем y");
        }else if (x == y) {
            System.out.println("x равен y");
        }else if (x < y) {
            System.out.println("y больше x");
        }
    }
}
